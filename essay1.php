<p>In the novel, Victor Frankenstein pushes the limits of science for its own sake,
without concern for the consequences of his actions. At the same time, emerging
European nation-states were pushing the limits of discovery and control over the Earth
and its inhabitants. Several times, Shelley directly analogizes Frankenstein's quest 
to physical exploration of the globe. The book can therefore be read as a critique 
of colonialism as an outgrowth of Europeans' insatiable desire to expand their 
knowledge and control over the physical world. I hope to employ GIS and maps as a form of 
multi-modal story telling to enhance this argument.</p>


